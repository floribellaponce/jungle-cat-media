<h2 align="center">Jungle Cat Media</h2>
<h3>Purpose</h3>
The purpose of this website will be to showcase Jungle Cat Media's work as a portfolio and also
to provide information on how to contact Jungle Cat Media for any inquiries and requests.
<h3>Audience</h3>
The type of audience targeted are business owners and personal brands. The focus is to reach any
business owner/personal brand that is looking for creative services such as videography.
<h3>Goal</h3>
The goal is to promote the services of Jungle Cat Media through the website so that
they can reach potential clients and take care of any of their videography needs.
